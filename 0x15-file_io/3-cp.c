#include <stdio.h>
#include <stdlib.h>
#include "main.h"

void close_file(int fd);
char *create_buffer(char *file);

/**
 * create_buffer - A buffer of 1024 bytes is allocated
 * @file: The file buffer's name is holding chars for
 *
 * Return: Pointer to the newly-allocated buffer
 */
char *create_buffer(char *file)
{
	char *buffer;

	buffer = malloc(sizeof(char) * 1024);

	if (buffer == NULL)
	{
		dprintf(STDERR_FILENO,
			"Error: Can't write to %s\n", file);
		exit(99);
	}

	return (buffer);
}

/**
 * close_file - The file descriptors are closed
 * @fd: The file descriptor that will be closed
 */
void close_file(int fd)
{
	int cl;

	cl = close(fd);

	if (cl == -1)
	{
		dprintf(STDERR_FILENO, "Error: Can't close fd %d\n", fd);
		exit(100);
	}
}

/**
 * main - The contents of a file is copied to another file
 * @argc: number of arguments passed to the program
 * @argv: Array of pointers to the arguments
 *
 * Return: 0 on success.
 *
 * Description: exit code 97 if the argument count is incorrect.
 *              exit code 98 if file_from does not exist or can't be read.
 *              exit code 99 if file_to can't be created or written to.
 *              exit code 100 if file_to or file_from ca't be closed
 */
int main(int argc, char *argv[])
{
	int from, to, r, w;
	char *buffer;

	if (argc != 3)
	{
		dprintf(STDERR_FILENO, "Usage: cp file_from file_to\n");
		exit(97);
	}

	buffer = create_buffer(argv[2]);
	from = open(argv[1], O_RDONLY);
	r = read(from, buffer, 1024);
	to = open(argv[2], O_CREAT | O_WRONLY | O_TRUNC, 0664);

	do {
		if (from == -1 || r == -1)
		{
			dprintf(STDERR_FILENO,
				"Error: Can't read from file %s\n", argv[1]);
			free(buffer);
			exit(98);
		}

		w = write(to, buffer, r);
		if (to == -1 || w == -1)
		{
			dprintf(STDERR_FILENO,
				"Error: Can't write to %s\n", argv[2]);
			free(buffer);
			exit(99);
		}

		r = read(from, buffer, 1024);
		to = open(argv[2], O_WRONLY | O_APPEND);

	} while (r > 0);

	free(buffer);
	close_file(from);
	close_file(to);

	return (0);
}

